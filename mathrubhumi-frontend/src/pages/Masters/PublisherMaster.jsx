        id: item.id,
        code: item.id.toString(),
        name: item.publisher_nm || '',
        contact: item.contact || '',
        own: item.own.toString(),
        email: item.email || '',
        address1: item.address1 || '',
        address2: item.address2 || '',
        phone: item.telephone || '',
        city: item.city || '',
        discount: item.max_discount_p.toString()
